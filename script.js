let searchBox = document.querySelector('#search-box');
//use keyup or keydown event-keyboard events
searchBox.addEventListener('keyup', function () {
  let textEntered = searchBox.value; //user entered text
  //console.log(textEntered);

  let filteredExperts = [];
  if (textEntered != '') {
    //populate data on search box

    filteredExperts = healthExperts.filter(function (healthExpert) {
      return (
        healthExpert.first_name
          .toLowerCase()
          .includes(textEntered.toLowerCase()) ||
        healthExpert.last_name
          .toLowerCase()
          .includes(textEntered.toLowerCase()) ||
        healthExpert.gender.toLowerCase() == textEntered.toLowerCase() ||
        healthExpert.dezignation
          .toLowerCase()
          .includes(textEntered.toLowerCase()) ||
        healthExpert.specialization
          .toLowerCase()
          .includes(textEntered.toLowerCase()) ||
        healthExpert.qualification
          .toLowerCase()
          .includes(textEntered.toLowerCase()) ||
        healthExpert.experience
          .toLowerCase()
          .includes(textEntered.toLowerCase())
      );
    });
    displayEmployess(filteredExperts);
  }
});

//create Display data function to get the data if user type
let displayEmployess = healthExperts => {
  //select the table
  let tableBody = document.querySelector('#table-body');

  //Initially the data in table will be empty so, clear the table body
  let tableRows = ''; //it will keep row empty before new data to display after clicking button otherwise it will display 1 data over another.
  for (let healthExpert of healthExperts) {
    tableRows += `
      <tr>
      <td>${healthExpert.dezignation}</td>
      <td>${healthExpert.first_name}</td>
      <td>${healthExpert.last_name}</td>
      <td>${healthExpert.email}</td>
      <td>${healthExpert.gender}</td>
      <td>${healthExpert.specialization}</td>
      <td>${healthExpert.qualification}</td>
      <td>${healthExpert.experience}</td>

      </tr>
      `;
    tableBody.innerHTML = tableRows;
    // tableBody.append(tableRows);
  }
};
